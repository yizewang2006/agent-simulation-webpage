package Utility;

public class DataStruct_ReserveProperty 
{
	public double value;
	public double weight;
	
	public DataStruct_ReserveProperty(double v, double w) 
	{
		this.value = v;
		this.weight = w;
	}
	
	public double getValue()
	{
		return this.value;
	}
	
	public double getWeight()
	{
		return this.weight;
	}
}
